package com.Java_Programs.Operators_Examples;

public class ComparisionOperatorsDemo {
	public static void main(String[] args) {
		int x = 20;
		int y = 10;
		System.out.println(x<y);
		System.out.println(x<=y);
		System.out.println(x>y);
		System.out.println(x>=y);
		System.out.println(x==20);
		System.out.println(x!=y);
		System.out.println(x!=10);


	}

}
