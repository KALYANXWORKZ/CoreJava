package com.Java_Programs.Operators_Examples;

public class AssignmentOperatorsDemo {
	public static void main(String[] args) {
		int x = 10;
		System.out.println("Value of x = "+x);
		x+=20;
		System.out.println("Value of x = "+x);
		
        int i = 45;
        System.out.println("Value of i = "+i);
        i-=13;
        System.out.println("Value of i = "+i);
   
	}
}