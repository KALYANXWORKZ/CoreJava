class AssignmentOperator
{
    public static void main(String[] args)
  {
     int x = 10;
     
     System.out.println("value of x ="+x);
     x+=15;
     System.out.println("value of x ="+x);

     int y = 10;
     System.out.println("value of y ="+y);
     y-=5;
     System.out.println("value of y ="+y);

   }
}